    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        String kode_kriteria = tKdKriteria.getText();
        String nama_kriteria = tNamaKriteria.getText();
        
        if (kode_kriteria.isEmpty() || nama_kriteria.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Isi semua kolom data kriteria terlebih dahulu!",
                    "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            // Panggil fungsi insertData() dari kelas Query untuk menyimpan data ke database
            String[] columns = {"kode_kriteria", "nama_kriteria"};
            Object[] values = {kode_kriteria, nama_kriteria};
            try {
                query.insertData(conn, "kriteria", columns, values);
                JOptionPane.showMessageDialog(this, "Data kriteria berhasil disimpan!",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);
                reset();
                datatable();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan data kriteria!",
                        "Error", JOptionPane.ERROR_MESSAGE);
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_bSimpanActionPerformed
