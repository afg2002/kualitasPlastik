    void datatable() {
        Object[] baris = {"Kode Kriteria", "Nama Kriteria"};
        tabmode = new DefaultTableModel(null, baris);
        tabDataKriteria.setModel(tabmode);
         String sql = "SELECT kode_kriteria, nama_kriteria FROM kriteria;";
        try {
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                String kode_kriteria = res.getString("kode_kriteria");
                String nama_kriteria = res.getString("nama_kriteria");

                String[] data = {kode_kriteria,nama_kriteria};
                tabmode.addRow(data);
            }
        } catch (SQLException ex) {
            System.out.println("Error: " + ex);
        }
        
    }

