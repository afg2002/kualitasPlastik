    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        int selectedRow = tabDataKriteria.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris data kriteria yang ingin dihapus!",
                    "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            String kode_kriteria = tKdKriteria.getText();
            int confirmDialog = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data kriteria?",
                    "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
            if (confirmDialog == JOptionPane.YES_OPTION) {
                // Panggil fungsi deleteData() dari kelas Query untuk menghapus data di database
                String condition = "kode_kriteria = '" + kode_kriteria + "'";
                try {
                    query.deleteData(conn, "kriteria", condition);
                    JOptionPane.showMessageDialog(this, "Data kriteria berhasil dihapus!",
                            "Informasi", JOptionPane.INFORMATION_MESSAGE);
                    reset();
                    datatable();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghapus data kriteria!",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_bHapusActionPerformed

    private void bEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditActionPerformed
        int selectedRow = tabDataKriteria.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris data kriteria yang ingin diedit!",
                    "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            String kode_kriteria = tKdKriteria.getText();
            String nama_kriteria = tNamaKriteria.getText();
            
            if (kode_kriteria.isEmpty() || nama_kriteria.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Isi semua kolom data kriteria terlebih dahulu!",
                        "Peringatan", JOptionPane.WARNING_MESSAGE);
            } else {
                // Panggil fungsi updateData() dari kelas Query untuk mengedit data di database
                String[] columns = {"kode_kriteria", "nama_kriteria"};
                Object[] values = {kode_kriteria, nama_kriteria};
                String condition = "kode_kriteria = '" + kode_kriteria + "'";
                try {
                    query.updateData(conn, "kriteria", columns, values, condition);
                    JOptionPane.showMessageDialog(this, "Data kriteria berhasil diupdate!",
                            "Informasi", JOptionPane.INFORMATION_MESSAGE);
                    reset();
                    datatable();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengupdate data kriteria!",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_bEditActionPerformed
